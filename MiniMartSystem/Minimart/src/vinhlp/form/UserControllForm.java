    private void loadForm() {
        UserDAO dao = new UserDAO();
        RoleDAO daoRole = new RoleDAO();
        try {
            List<UserDTO> dtos = dao.getAllUser();
            //3. reset lại bảng
            tblModelUser.setRowCount(0);
            //4.duyệt các đối tượng trong list
            for (UserDTO dto : dtos) {
                tblModelUser.addRow(dto.toVector());
            }
            daoRole.getAllRole();
            List<String> role = daoRole.getListRole();
            cbxGroup.removeAllItems();
            for (String item : role) {
                cbxGroup.addItem(item);
            }
        } catch (ClassNotFoundException e) {
            System.out.println("loadFrom: " + e.getMessage());
        } catch (SQLException e) {
            System.out.println("loadForm: " + e.getMessage());
        }
        txtPassword.setEditable(false);
        txtUsername.setEditable(false);
        btnSavepass.setVisible(false);
        lbGroup.setVisible(false);
        cbxGroup.setVisible(false);
        btnSaveGroup.setVisible(false);
    }

