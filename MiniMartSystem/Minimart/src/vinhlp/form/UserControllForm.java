    private void btnSavepassActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSavepassActionPerformed
        String username = txtUsername.getText();
        String password = txtPassword.getText();
        int role = cbxGroup.getSelectedIndex();
        if (role > 0) {
            try {
                //call dao to change password
                UserDAO dao = new UserDAO();
                boolean isUpdatePassword = dao.changePassword(username, password);
                if (isUpdatePassword) {
                    JOptionPane.showMessageDialog(this, "Update Success", "Update Process", HEIGHT);
                    loadForm();
                } else {
                    JOptionPane.showMessageDialog(this, "Update fail", "Update Process", HEIGHT);
                    loadForm();
                }
            } catch (SQLException | ClassNotFoundException e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Update Process", HEIGHT);
            }
        } else {
            JOptionPane.showMessageDialog(this, "You can't change the admin password!", "Update Process", HEIGHT);
            loadForm();
        }
    }//GEN-LAST:event_btnSavepassActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String username = txtUsername.getText();
        int role = cbxGroup.getSelectedIndex();
        if (role > 0) {
            UserDAO dao = new UserDAO();
            try {
                boolean isDelete = dao.deleteUser(username);
                if (isDelete) {
                    JOptionPane.showMessageDialog(this, "Delete Success!", "Delete User process: ", HEIGHT);
                } else {
                    JOptionPane.showMessageDialog(this, "Delete Fail!", "Delete User process: ", HEIGHT);
                }
            } catch (SQLException | ClassNotFoundException e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Delete User process: ", HEIGHT);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Can't delete the admin account!", "Delete User process: ", HEIGHT);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSaveGroupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveGroupActionPerformed
        String username = txtUsername.getText();
        int roleNum = cbxGroup.getSelectedIndex() + 1;
        UserDAO dao = new UserDAO();
        try {
            if (roleNum == 3) {
                //check is the inventory staff is exist!
                boolean isExistInventoryStaff = dao.isExistInventoryStaff();
                if (isExistInventoryStaff) {
                    JOptionPane.showMessageDialog(this, "There is only inventory staff in the company!", "Update Role Process:", HEIGHT);
                } // end if not exist inventory staff
                else {
                    boolean isUpdateRole = dao.changeRole(username, roleNum);
                    if (isUpdateRole) {
                        JOptionPane.showMessageDialog(this, "Update Role Success!", "Update Role Process:", HEIGHT);
                        loadForm();
                    } else {
                        JOptionPane.showMessageDialog(this, "Update Role Fail!", "Update Role Process:", HEIGHT);
                    }
                }
            } else if (dao.isAdminUser(username)) {
                JOptionPane.showMessageDialog(this, "You can't change the group of admin", "Update Role Process: ", HEIGHT);
                loadForm();
            } else {
                boolean isUpdateRole = dao.changeRole(username, roleNum);
                if (isUpdateRole) {
                    JOptionPane.showMessageDialog(this, "Update Role Success!", "Update Role Process:", HEIGHT);
                    loadForm();
                } else {
                    JOptionPane.showMessageDialog(this, "Update Role Fail!", "Update Role Process:", HEIGHT);
                }
            }
        } catch (SQLException | ClassNotFoundException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Update Role Process:", HEIGHT);
            loadForm();
        }
    }//GEN-LAST:event_btnSaveGroupActionPerformed

