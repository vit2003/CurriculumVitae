    private void tblUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUserMouseClicked
        int row = 1;
        row = tblUser.getSelectedRow();
        String username = (String) tblModelUser.getValueAt(row, 0);
        txtUsername.setText(username);
        String password = (String) tblModelUser.getValueAt(row, 1);
        txtPassword.setText(password);
        String role = (String) tblModelUser.getValueAt(row, 2);
        int index = 0;
        if (role.equals("Seller")) {
            index = 1;
        } else if (role.equals("Inventory Staff")) {
            index = 2;
        }
        cbxGroup.setSelectedIndex(index);
    }//GEN-LAST:event_tblUserMouseClicked

