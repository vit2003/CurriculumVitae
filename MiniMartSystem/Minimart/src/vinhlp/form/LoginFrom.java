private void loadLanguage(){
    Locale l = null;
     if(rbtnVn.isSelected()){
         l = new Locale("VN", "vn");
     }else {
         l = new Locale("US", "en");
     }
     //load file properties
     ResourceBundle rbd;
        rbd = ResourceBundle.getBundle("vinhlp.inter/resource",l);
        lbBig.setText(rbd.getString("biglabel"));
        lbUsername.setText(rbd.getString("username"));
        lbPassword.setText(rbd.getString("password"));
        rdbEnglish.setText(rbd.getString("english"));
        rbtnVn.setText(rbd.getString("vne"));
}

