    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String username = txtUsername.getText();
        String password = txtPassword.getText();
        UserDAO dao = new UserDAO();
        try {
            UserDTO dto = dao.checkLogin(username, password);
            if (dto == null) {
                JOptionPane.showMessageDialog(this, "Invalid Username/Password", "Login Annotation: ", JOptionPane.INFORMATION_MESSAGE);
            } else {
                new MainFrame(dto).setVisible(true);
                this.dispose();
            }
        } catch (SQLException | ClassNotFoundException e){
            System.out.println("Login Process: "+e.getMessage());
        }
    }//GEN-LAST:event_btnLoginActionPerformed

