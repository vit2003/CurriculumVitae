    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        CategoryDAO dao = new CategoryDAO();
        try {
            if (action.equals("update")) {
                boolean result = dao.changeNameOfCategory(txtCateID.getText(), txtCateName.getText());
                if (result) {
                    JOptionPane.showMessageDialog(this, "Save success!", "Update category: ", HEIGHT);
                    loadForm();
                } else {
                    JOptionPane.showMessageDialog(this, "Save fail!", "Update category: ", HEIGHT);
                    loadForm();
                }
            } else if (action.equals("add")) {
                boolean result = dao.createCategory(txtCateID.getText(), txtCateName.getText());
                if (result) {
                    JOptionPane.showMessageDialog(this, "Save success!", "Create new category: ", HEIGHT);
                    loadForm();
                } else {
                    JOptionPane.showMessageDialog(this, "Save fail!", "Create new category: ", HEIGHT);
                    loadForm();
                }
            }
        } catch (SQLException | ClassNotFoundException e) {
            System.out.println("Save process: " + e.getMessage());
            if (e.getMessage().contains("duplicate")) {
                try {
                    boolean resultC = dao.returnCategory(txtCateID.getText(),txtCateName.getText());
                    if (resultC) {
                        JOptionPane.showMessageDialog(this, "Save success!", "Create new category: ", HEIGHT);
                        loadForm();
                    } else {
                        JOptionPane.showMessageDialog(this, "Save fail!", "Create new category: ", HEIGHT);
                        loadForm();
                    }
                } catch (SQLException | ClassNotFoundException ex) {
                    System.out.println("Save category: " + ex.getMessage());
                }
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

