    private void lsCategoryMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lsCategoryMouseClicked
        txtCateID.setEditable(false);
        txtCateName.setEditable(false);
        btnSave.setVisible(false);
        btnDeleteCate.setEnabled(true);
        btnAddCate.setEnabled(true);
        btnUpdateCate.setEnabled(true);
        CategoryDAO dao = new CategoryDAO();
        ProductDAO daoPro = new ProductDAO();

        String categoryName = lsCategory.getSelectedValue();
        try {
            String categoryID = dao.getCategoryID(categoryName);
            txtCateID.setText(categoryID);
            txtCateName.setText(categoryName);
            daoPro.loadProductFollowCategory(categoryID);
            List<ProductDTO> alist = daoPro.getListDTO();
            tblModelProduct.setRowCount(0);
            for (ProductDTO dto : alist) {
                tblModelProduct.addRow(dto.toVector());
            }
        } catch (SQLException | ClassNotFoundException e) {
            System.out.println("load product process: " + e.getMessage());
        }
    }//GEN-LAST:event_lsCategoryMouseClicked

    private void tblProductMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProductMouseClicked
        int row = tblProduct.getSelectedRow();
        txtProductID.setText((String) tblModelProduct.getValueAt(row, 0));
        txtProductName.setText((String) tblModelProduct.getValueAt(row, 1));
        txtProductPrice.setText((String) tblModelProduct.getValueAt(row, 2).toString());
        StringTokenizer stk = new StringTokenizer((String) tblModelProduct.getValueAt(row, 3), "-");
        String year = "";
        int month = 1, day = 1;
        while (stk.hasMoreTokens()) {
            year = stk.nextToken();
            month = Integer.parseInt(stk.nextToken());
            day = Integer.parseInt(stk.nextToken());
        }
        txtPackagingYear.setText(year);
        cbxDay.setSelectedIndex(day - 1);
        cbxMonth.setSelectedIndex(month - 1);
    }//GEN-LAST:event_tblProductMouseClicked

