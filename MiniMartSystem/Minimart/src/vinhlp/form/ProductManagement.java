    private void btnSaveProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveProductActionPerformed
        String id = txtProductID.getText();
        if (!id.equals("")) {
            String name = txtProductName.getText();
            try {
                int day = cbxDay.getSelectedIndex() + 1;
                int month = cbxMonth.getSelectedIndex() + 1;
                int year = Integer.parseInt(txtPackagingYear.getText());
                String date = year + "-" + month + "-" + day;
                float price = Float.parseFloat(txtProductPrice.getText());
                //call DAO to update product
                ProductDAO dao = new ProductDAO();
                boolean result = dao.updateProduct(id, name, price, date);
                if (result) {
                    JOptionPane.showMessageDialog(this, "Update Succesful!", "Update process: ", HEIGHT);
                    loadForm();
                } else {
                    JOptionPane.showMessageDialog(this, "Update Fail!", "Update process: ", HEIGHT);
                }
            } catch (SQLException | ClassNotFoundException e) {
                if (e.getMessage().contains("date and/or time")) {
                    JOptionPane.showMessageDialog(this, "Invalid date!\n select date again please", "Update process: ", WIDTH);
                }
                System.out.println("Update product process: " + e.getMessage());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "The price you edited isn't the float number", "Update process: ", HEIGHT);
                System.out.println("Update process: " + e.getMessage());
            }
        }//end if ID isn't null
    }//GEN-LAST:event_btnSaveProductActionPerformed

